class TestPostCommentCommand:
    """Tests for post_comment_command CLI command."""

    def test_post_comment_command_exists(self):
        """Post-comment command should be registered."""
        assert "post-comment" in cli.commands

    def test_post_comment_requires_token(self):
        """Exit with code 1 when no token provided and GITHUB_TOKEN not set."""
        with tempfile.NamedTemporaryFile(mode='w', suffix='.jsonl', delete=False) as f:
            f.write('{"skillPath": "test.md", "skillName": "test", "overallPassed": true, "blockers": [], "warnings": [], "hints": [], "timestamp": "2026-04-19T10:00:00Z"}\n')
            lint_file = f.name

        try:
            with patch.dict("os.environ", {}, clear=True):
                runner = CliRunner()
                result = runner.invoke(post_comment_command, [
                    "--diff", "--- a/test.md\n+++ b/test.md",
                    "--lint-report", lint_file,
                    "--pr", "2341",
                    "--repo", "owner/repo",
                ])

            assert result.exit_code == 1
            assert "GITHUB_TOKEN env not set" in result.output
        finally:
            import os
            os.unlink(lint_file)
