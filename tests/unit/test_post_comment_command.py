# We need to import from cli.py which is a module file, not the cli/ package
# Approach: Load cli.py into a fake module, then copy its attributes to the real cli package

# First, ensure the cli package submodules are loaded
from aft.cli.router import DiffRouter, DiffRouterResult, RuleFileChange, SkillFileChange, CodeChange
from aft.cli.parsers.lint import LintResultParser, LintReport, LintResult
from aft.cli.github import GitHubCommentPoster

# Now save the cli package module
_cli_package = sys.modules.get('aft.cli')

# Remove the cli package temporarily
if 'aft.cli' in sys.modules:
    del sys.modules['aft.cli']

# Load cli.py as a fresh module
_cli_path = '/Users/lijingnan/Desktop/project/test_haress_engine/aft/src/aft/cli.py'
_spec = importlib.util.spec_from_file_location('_cli_module', _cli_path)
_cli_module = importlib.util.module_from_spec(_spec)
_spec.loader.exec_module(_cli_module)

# Now we need to make aft.cli point to _cli_module
# But we also need to ensure that 'from aft.cli.router import X' still works
# So we need to merge _cli_module's attributes into _cli_package

if _cli_package is not None:
    # Copy all attributes from _cli_module to _cli_package
    for attr in dir(_cli_module):
        if not attr.startswith('_'):
            setattr(_cli_package, attr, getattr(_cli_module, attr))
    # Restore sys.modules['aft.cli'] to the package
    sys.modules['aft.cli'] = _cli_package
else:
    # No existing package, just use the module
    sys.modules['aft.cli'] = _cli_module

# Extract what we need from the module
build_skill_harness_comment = _cli_module.build_skill_harness_comment
post_comment_command = _cli_module.post_comment_command
cli = _cli_module.cli


